# hay 3 operaores logicos


# AND 
# se cumplira si ambas son iguales


resultado1 = True & True # Devulver True
resultado2 = False & False # Devulver false
resultado3 = True & False # Devulver false
resultado4 = False & False # Devulver false


# OR
# nos mandara true solo si las dos condiciones son faltas
# si hay algun true dentro de las condiciones sera verdadera

resultado5 = True | True # Devulver True
resultado6 = False | True # Devulver True
resultado7 = True | False # Devulver True
resultado8 = False | False # Devulver false

#NOT
# aqui mismo es intuitivo 
# Hay que tener en cuenta que todo lo que rebote a un valor booleando se convertira a su negativo, ya que es lo que hace la condicion NOT


resultado9 = not True # Devulver false
resultado10 = not False # Devulver True


